class STRINGX
{
    public void strRevX(String str)
    {

        int iCnt = 0;

        char Arr[] = str.toCharArray();

        for(iCnt = (Arr.length - 1); iCnt >= 0; iCnt--)
        {
            System.out.print(Arr[iCnt]);
        }


    }
}
